public class checkIfItsEvenOrOdd {
    
    public static void main(String[] args) {
    
        int n = 50;

        for (int i = 0; i < n; i++) {
            if (i % 2 != 0) {
                System.out.println("Odd Number: " + i);
            } else {
                System.out.println("Even Number: " + i);
            }

        }

    }

}
